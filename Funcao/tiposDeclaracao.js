// function declaration
function soma(x, y) {
    return x + y
}

// function expression
const sub = function (x, y) {
    return x - y
}

// named function expression
const mult = function mult(x, y) {
    return x * y
}

/* a diferença entre as 3: declaration pode ser chamada anteriormente à
função, enquanto expressior e named só podem ser chamadas depois de 
declaradas */